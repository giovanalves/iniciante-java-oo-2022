package movimento_codar;

public class Aprendiz {

		
	private String acessaAulas;
	private String resultado;
	private String pergunta;	
	private String participaEvento;
	private Pessoa pessoa;
	
	
	 public Pessoa getPessoa() {
		return pessoa;
	}

	public void setPessoa(Pessoa pessoa) {
		this.pessoa = pessoa;
	}

	public String getAcessaAulas() {
		return acessaAulas;
	}

	public void setAcessaAulas(String acessaAulas) {
		this.acessaAulas = acessaAulas;
	}

	public String getResultado() {
		return resultado;
	}

	public void setResultado(String resultado) {
		this.resultado = resultado;
	}

	public String getPergunta() {
		return pergunta;
	}

	public void setPergunta(String pergunta) {
		this.pergunta = pergunta;
	}

	public String getParticipaEvento() {
		return participaEvento;
	}

	public void setParticipaEvento(String participaEvento) {
		this.participaEvento = participaEvento;
	}

	
}

